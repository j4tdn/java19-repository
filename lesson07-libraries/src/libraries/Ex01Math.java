package libraries;

public class Ex01Math {
	public static void main(String[] args) {
		System.out.println("min --> " + Math.min(5.6d, 6.7d));
		
		float fVal = 10f/3;
		
		System.out.println("fVal --> " + fVal);
		
		System.out.println("round --> " + Math.round(fVal));
		
		System.out.println("roundUp --> " + Math.ceil(fVal));
		
		
		
		System.out.println("roundDown --> " + Math.floor(fVal));
	}
}
